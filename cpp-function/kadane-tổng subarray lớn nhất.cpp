// Function to find subarray with maximum sum
// arr: input array
// n: size of array
int maxSubarraySum(int arr[], int n){
    
    // Your code here
    int i,s=0,b=0;
    for(i=0;i<n;i++){
    s=max(arr[i],s+arr[i]);
    b=max(s,b);}
    return b;
    
}

